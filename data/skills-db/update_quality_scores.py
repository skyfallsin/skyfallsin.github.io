def update_database():
    """Update database with all collected scores."""
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()
    
    # Ensure quality_rationale column exists
    try:
        cur.execute("ALTER TABLE skills ADD COLUMN quality_rationale TEXT")
    except sqlite3.OperationalError:
        pass  # Column already exists
    
    updated = 0
    not_found = []
    
    for skill in SKILLSSH_SCORES:
        name = skill["name"]
        score = skill["score"]
        rationale = f"[manual] {skill.get('rationale', '')}"
        
        # Try to match by name in skillssh registry
        cur.execute("""
            UPDATE skills 
            SET quality_score = ?, quality_rationale = ?
            WHERE registry = 'skillssh' AND (name = ? OR slug LIKE ?)
        """, (score, rationale, name, f"%{name}%"))
        
        if cur.rowcount > 0:
            updated += cur.rowcount
        else:
            not_found.append(name)
    
    conn.commit()
    
    # Print summary
    cur.execute("""
        SELECT registry, 
               COUNT(*) as count,
               ROUND(AVG(quality_score), 1) as avg_score,
               SUM(CASE WHEN quality_rationale LIKE '[manual]%' THEN 1 ELSE 0 END) as manual_count
        FROM skills 
        GROUP BY registry
    """)
    
    print("Quality Score Summary by Registry:")
    print("-" * 70)
    for row in cur.fetchall():
        print(f"{row[0]:20} | count: {row[1]:4} | avg: {row[2]:5.1f} | manual: {row[3]}")
    
    print(f"\nUpdated {updated} skills with manual scores")
    if not_found:
        print(f"Could not find {len(not_found)} skills: {not_found[:10]}...")
    
    conn.close()

if __name__ == "__main__":
    update_database()
